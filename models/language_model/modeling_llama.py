class LlamaForImageClassification(LlamaPreTrainedModel):
    def __init__(self, config):
        super().__init__(config)
        self.num_labels = 1000
        self.vqgan_embed_dim = 768
        self.image_size = 16

        # Create modified Llama model
        self.model = LlamaModel(config)
        
        # Remove RoPE from attention layers
        for layer in self.model.layers:
            if hasattr(layer.self_attn, 'rotary_emb'):
                delattr(layer.self_attn, 'rotary_emb')

        # Add learnable position embeddings
        self.pos_embed = nn.Parameter(torch.zeros(1, self.image_size * self.image_size + 1, config.hidden_size))
        self.cls_token = nn.Parameter(torch.zeros(1, 1, config.hidden_size))

        # Projection from VQGAN space to Llama space
        self.vqgan_projection = nn.Linear(self.vqgan_embed_dim, config.hidden_size)

        # Classification head
        self.dropout_prob = 0.1  # Explicitly define dropout probability
        self.dropout = nn.Dropout(self.dropout_prob)
        self.classifier = nn.Linear(config.hidden_size, self.num_labels)

        # Initialize weights and apply final processing
        self.post_init()

    def forward(
        self,
        vqgan_embeddings: torch.FloatTensor,
        attention_mask: Optional[torch.Tensor] = None,
        labels: Optional[torch.LongTensor] = None,
        output_attentions: Optional[bool] = None,
        output_hidden_states: Optional[bool] = None,
        return_dict: Optional[bool] = None,
    ) -> Union[Tuple, ImageClassifierOutput]:
        return_dict = return_dict if return_dict is not None else self.config.use_return_dict

        # Project VQGAN embeddings to Llama input space
        inputs_embeds = self.vqgan_projection(vqgan_embeddings)

        # Add CLS token
        batch_size = inputs_embeds.shape[0]
        cls_tokens = self.cls_token.expand(batch_size, -1, -1)
        inputs_embeds = torch.cat((cls_tokens, inputs_embeds), dim=1)

        # Add positional embeddings
        inputs_embeds = inputs_embeds + self.pos_embed

        # Adjust attention mask for CLS token
        if attention_mask is not None:
            attention_mask = torch.cat([torch.ones(batch_size, 1, device=attention_mask.device), attention_mask], dim=1)

        outputs = self.model(
            inputs_embeds=inputs_embeds,
            attention_mask=attention_mask,
            output_attentions=output_attentions,
            output_hidden_states=output_hidden_states,
            return_dict=return_dict,
            use_cache=False,  # we don't need caching for classification
        )
        
        # Use the CLS token output for classification
        cls_output = outputs[0][:, 0]
        
        cls_output = self.dropout(cls_output)
        logits = self.classifier(cls_output)

        loss = None
        if labels is not None:
            loss_fct = nn.CrossEntropyLoss()
            loss = loss_fct(logits, labels)

        if not return_dict:
            output = (logits,) + outputs[2:]
            return ((loss,) + output) if loss is not None else output

        return ImageClassifierOutput(
            loss=loss,
            logits=logits,
            hidden_states=outputs.hidden_states,
            attentions=outputs.attentions,
        )